// Helper to filter prefixes by search query (code or country name)
export function filterPhonePrefixes(prefixes: PhonePrefix[], search: string): PhonePrefix[] {
  if (!search.trim()) return prefixes;
  const normalizedSearch = search.replace(/[+-]/g, '').toLowerCase();
  return prefixes.filter(prefix =>
    prefix.code.replace(/[+-]/g, '').includes(normalizedSearch) ||
    prefix.country.toLowerCase().includes(normalizedSearch)
  );
}

// Helper to find a prefix by code
export function findPrefixByCode(code: string): PhonePrefix | undefined {
  return PHONE_PREFIXES.find(p => p.code === code);
}
